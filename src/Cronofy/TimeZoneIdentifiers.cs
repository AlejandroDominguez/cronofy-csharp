namespace Cronofy
{
    /// <summary>
    /// Class for time zone identifier constants.
    /// </summary>
    internal static class TimeZoneIdentifiers
    {
        /// <summary>
        /// The time zone identifier for Coordinated Universal Time (UTC).
        /// </summary>
        public const string UTC = "Etc/UTC";

        /// <summary>
        /// The default time zone identifier for times.
        /// </summary>
        public const string Default = UTC;
    }
}
